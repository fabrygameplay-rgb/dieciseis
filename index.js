resultsBox.style.zIndex = "9999";


const mediaCelular = window.matchMedia("(max-width: 480px)");

function ajustarInput(e) {

    if (e.matches) {
        inputSearcher.style.width = "90px";
        inputSearcher.style.fontSize = "9px";
        inputSearcher.style.marginLeft = "10px";
        resultsBox.style.width = "150px";
        resultsBox.style.left = "-10px";
    } 
    
    else {
        inputSearcher.style.width = "140px";
        resultsBox.style.width = "200px";
        resultsBox.style.left = "-30px";
    }

}

ajustarInput(mediaCelular);
mediaCelular.addEventListener("change", ajustarInput);

searcher.appendChild(inputSearcher);
searcher.appendChild(resultsBox);

searcher.addEventListener("click", (e) => {
    e.stopPropagation();
    inputSearcher.style.display = "block";
    inputSearcher.focus();
});

document.addEventListener("click", (e) => {

    if (!searcher.contains(e.target)) {
        inputSearcher.style.display = "none";
        inputSearcher.value = "";
        resultsBox.style.display = "none";
    }

});

inputSearcher.addEventListener("input", () => {

    const text = inputSearcher.value.toLowerCase().trim();

    resultsBox.innerHTML = "";
    selectedIndex = -1;

    if (!text) {
        resultsBox.style.display = "none";
        return;
    }

    currentResults = Object.values(sinopsisJuegos).filter(game =>
        game.name.toLowerCase().includes(text)
    );

    if (currentResults.length === 0) {
        resultsBox.style.display = "none";
        return;
    }

    currentResults.forEach(game => {

        const item = document.createElement("div");
        item.textContent = game.name;
        item.style.padding = "3px";
        item.style.cursor = "pointer";
        item.style.fontSize = "12px";

        item.addEventListener("click", () => {
            const searchText = encodeURIComponent(inputSearcher.value.trim());
            window.location.href = `${game.page}?search=${searchText}`;
        });

        item.addEventListener("mouseover", () => {
            item.style.background = "#eee";
        });

        item.addEventListener("mouseout", () => {
            item.style.background = "white";
        });

        resultsBox.appendChild(item);
    });

    resultsBox.style.display = "block";

});

inputSearcher.addEventListener("keydown", (e) => {

    const items = resultsBox.querySelectorAll("div");

    if (!items.length) return;

    if (e.key === "ArrowDown") {

        selectedIndex++;
        if (selectedIndex >= items.length) selectedIndex = 0;

        updateSelection(items);
    }

    if (e.key === "ArrowUp") {

        selectedIndex--;
        if (selectedIndex < 0) selectedIndex = items.length - 1;

        updateSelection(items);
    }

    if (e.key === "Enter") {

        if (selectedIndex >= 0) {
            items[selectedIndex].click();
        }
    }

});

function updateSelection(items) {

    items.forEach(item => item.style.background = "white");

    items[selectedIndex].style.background = "#ddd";
}

function filterGamesFromURL() {

    const params = new URLSearchParams(window.location.search);
    const search = params.get("search");

    if (!search) return;

    const text = search.toLowerCase();

    document.querySelectorAll(".game").forEach(game => {

        const name = game.dataset.game;
        const info = sinopsisJuegos[name];

        if (!info) return;

        const match = info.name.toLowerCase().includes(text);

        game.style.display = match ? "" : "none";

    });
}

filterGamesFromURL();

const navPrimary = document.querySelector(".logOut");
const navSecondary = document.querySelector(".deleteAccount");

function changeNav(status) {

    switch(status) {

        case "newUser":

            navPrimary.textContent = "Sign in";
            navPrimary.href = "indexAccount1.html";

            navSecondary.textContent = "Log in";
            navSecondary.href = "indexAccount2.html";

        break;

        case "oldUser":

            navPrimary.textContent = "Log Out";
            navPrimary.href = "#";

            navSecondary.textContent = "Delete Account";
            navSecondary.href = "#";

        break;
    }

}

const signInForm = document.querySelector("#formContent1");

const logInForm = document.querySelector("#formContent2 form");

[signInForm, logInForm].forEach(form => {

    if(!form) return;

    form.addEventListener("submit", (e) => {

        e.preventDefault();

        localStorage.setItem("authState", "oldUser");

        changeNav("oldUser");

    });

});

navPrimary.addEventListener("click", () => {

    if(localStorage.getItem("authState") === "oldUser") {

        localStorage.removeItem("authState");

        changeNav("newUser");

    }

});

navSecondary.addEventListener("click", () => {

    if(localStorage.getItem("authState") === "oldUser") {

        localStorage.clear();

        alert("Account deleted");

        changeNav("newUser");

    }

});

window.addEventListener("DOMContentLoaded", () => {

    const status = localStorage.getItem("authState");

    if(status === "oldUser") {
        changeNav("oldUser");
    } else {
        changeNav("newUser");
    }

});